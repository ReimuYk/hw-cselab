// disk layer -----------------------------------------

disk::disk()
{
  bzero(blocks, sizeof(blocks));
}

void
disk::read_block(blockid_t id, char *buf)
{
  buf = new char[BLOCK_SIZE];
  for (int i=0;i<BLOCK_SIZE;i++){
    buf[i] = blocks[id][i];
  }
}

void
disk::write_block(blockid_t id, const char *buf)
{
  for (int i=0;i<BLOCK_SIZE;i++){
    blocks[id][i] = buf[i];
  }
}

